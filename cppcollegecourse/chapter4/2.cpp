#include <iostream>
using namespace std;
int main()
{
    int x = 1;
    x = x+1;
    cout << "x now is " << x << endl;
    x++;
    cout << "x now is " << x << endl;
    ++x;
    cout << "x now is " << x << endl;
    x+=1;
    cout << "x now is " << x << endl;
    x = x+1;
    cout << "x now is " << x << endl;
    return 0;
}
